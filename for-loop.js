// Declares a variable named total and sets it equal to 0
var total = 0;
// Declares a variable named limit and sets it equal to 10;
var limit = 10;

// For loop which iterates 10 times
for (i=0; i < limit; i++) {
  // add i to total
  total += i;
}
// Log the total after 10 iterations to the Console
console.log(total);
